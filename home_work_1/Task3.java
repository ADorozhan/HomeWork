package home_work_1;
//Изучить библиотеку (класс) Math
public class Task3 {
    public static void main(String[] args) {
        System.out.println( Math.abs(-3));//вычислить значение по модулю
        System.out.println(  Math.max(-10, 7));//вычислить мах из 2 чисел
        System.out.println(  Math.min(-10, 7));//вычислить min из 2 чисел
        System.out.println(Math.pow(3,2));// возведения 3 в степень 2
        System.out.println(Math.sqrt(16));// извлекает квадратный корень
        System.out.println(Math.random());// Этот метод может генерировать только
                                          // случайные числа типа Doubles.
        System.out.println(Math.round(1.6));//округляет числа
    }
}
